LO, HI = 10.0, 38.0  # score axis for the bars (gap-to-leader visible)


def _esc(s) -> str:
    return (str(s).replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;"))


def _fill_pct(score: float) -> float:
    return max(2.0, min(100.0, (score - LO) / (HI - LO) * 100.0))


def _color(verdict: Optional[str]) -> str:
    return {"win": "var(--win)", "loss": "var(--loss)", "flat": "var(--flat)"}.get(verdict, "var(--queue)")


def render_html(data: dict) -> str:
    m = data["meta"]
    subs = data["submissions"]
    best = m.get("best_score")
    leader = m.get("leader_target", 37.0)
    gap_pct = (best / leader * 100.0) if best else 0.0
    lead_x = (leader - LO) / (HI - LO) * 100.0

    scored = [s for s in subs if s.get("status") == "scored" and s.get("score") is not None]
    scored.sort(key=lambda s: (s["date"], s["score"]))
    queued = [s for s in subs if s.get("status") == "queued"]
    held = [s for s in subs if s.get("status") == "held"]

    h: List[str] = []
    h.append("<!doctype html><html lang=en><head><meta charset=utf-8>")
    h.append("<meta name=viewport content='width=device-width,initial-scale=1'>")
    h.append(f"<title>Decision Dashboard — {_esc(m['challenge'])}</title>")
    h.append(f"<style>{CSS}</style></head><body><div class=wrap>")

