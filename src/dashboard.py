    h.append(f"<h1>{_esc(m['challenge'])}</h1>")
    h.append(f"<div class=sub>{_esc(m['prize'])} · updated {_esc(m['updated'])} · "
             "single source of truth: <code>outputs/dashboard/submissions.json</code></div>")
    h.append("<div class=card><div class=grid>")
    h.append(f"<div class=kpi><div class=big>{best:.2f}</div><div class=lbl>best public score "
             f"({_esc(m.get('best_id'))})</div></div>")
    h.append(f"<div class=kpi><div class=big>{leader:.0f}</div><div class=lbl>leader target</div></div>")
