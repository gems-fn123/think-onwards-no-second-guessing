"""
Evergreen decision-analysis dashboard + score porter.

Single source of truth:  outputs/dashboard/submissions.json
One command ports every score update to ALL of:
  * the JSON                 (outputs/dashboard/submissions.json)
  * the HTML dashboard       (outputs/dashboard/index.html, self-contained)
  * the memory snapshot      (<memory>/scoreboard.json + a block in the memory md)
  * the agent work log       (a block in SUBMISSIONS.md)

    python -m src.dashboard                    # regenerate everything from JSON
    python -m src.dashboard --set P1=22.90      # set score(s), recompute, regenerate
    python -m src.dashboard --set P1=22.9 --set C2=21.4

A score set on a "queued"/"held" submission flips it to "scored" automatically and
fires its pre-registered decision rule, so the verdict appears the moment the
number lands. Derived fields (axis ratio, delta, verdict) are recomputed on every
run from the raw scores — nothing derived is stored, so the data can never drift.
"""

from __future__ import annotations

import argparse
import json
import os
from datetime import datetime
from typing import Dict, List, Optional

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
DASH_DIR = os.path.join(ROOT, "outputs", "dashboard")
JSON_PATH = os.path.join(DASH_DIR, "submissions.json")
HTML_PATH = os.path.join(DASH_DIR, "index.html")
SUBMISSIONS_MD = os.path.join(ROOT, "SUBMISSIONS.md")
DEFAULT_MEMORY_DIR = os.path.join(
    os.path.expanduser("~"),
    ".claude", "projects", "d--ES-DRJ-think-onwards-no-second-guessing", "memory",
)
MEMORY_MD_NAME = "project-no-second-guessing.md"

BLOCK_START = "<!-- SCOREBOARD:START (auto-generated by src/dashboard.py — do not edit by hand) -->"
BLOCK_END = "<!-- SCOREBOARD:END -->"


# ---------------------------------------------------------------------------
# Data
# ---------------------------------------------------------------------------
def load() -> dict:
    with open(JSON_PATH, encoding="utf-8") as fh:
        return json.load(fh)


def save(data: dict) -> None:
    with open(JSON_PATH, "w", encoding="utf-8") as fh:
        json.dump(data, fh, indent=2, ensure_ascii=False)
        fh.write("\n")


def _by_id(subs: List[dict]) -> Dict[str, dict]:
    return {s["id"]: s for s in subs}


def enrich(data: dict) -> dict:
    """Recompute every derived field from raw scores. Pure; mutates copies only."""
    subs = data["submissions"]
    idx = _by_id(subs)

    scored = [s for s in subs if s.get("status") == "scored" and s.get("score") is not None]
    best = max(scored, key=lambda s: s["score"]) if scored else None
    data["meta"]["best_id"] = best["id"] if best else None
    data["meta"]["best_score"] = best["score"] if best else None
    data["meta"]["updated"] = datetime.now().strftime("%Y-%m-%d %H:%M")

    for s in subs:
        anchor = idx.get(s.get("anchor")) if s.get("anchor") else None
        a_score = anchor["score"] if anchor and anchor.get("score") else None
        s["_anchor_score"] = a_score
        s["_ratio"] = None
        s["_delta"] = None
        s["_verdict"] = None
        if s.get("status") == "scored" and s.get("score") is not None and a_score:
            ratio = (s["score"] / a_score) ** 4
            s["_ratio"] = round(ratio, 3)
            s["_delta"] = round(s["score"] - a_score, 2)
            if ratio > 1.02:
                s["_verdict"] = "win"
            elif ratio < 0.98:
                s["_verdict"] = "loss"
            else:
                s["_verdict"] = "flat"
    return data


def apply_sets(data: dict, sets: List[str]) -> None:
    idx = _by_id(data["submissions"])
    for item in sets:
        if "=" not in item:
            raise SystemExit(f"--set expects ID=SCORE, got {item!r}")
        sid, val = item.split("=", 1)
        sid = sid.strip()
        if sid not in idx:
            raise SystemExit(f"unknown submission id {sid!r}; known: {', '.join(idx)}")
        idx[sid]["score"] = float(val)
        idx[sid]["status"] = "scored"
        print(f"  set {sid} = {val} (status -> scored)")


